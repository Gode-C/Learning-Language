import math



def sd(l):

    m = list()



    

    for i in range(len(l)):
        x = i
        for e in range(len(l)):
            if x != e:
                y = e
                k = ((l[x])[0] - (l[y])[0]) ** 2 + ((l[x])[1] - (l[y])[1]) ** 2
                k = math.sqrt(k)
                m = m + [k]


    return print(min(m))


